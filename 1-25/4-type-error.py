a = '1'

b = 2

print(int(a) + b)

# Explanation: 

# Values in Python can be of different types. 
# In this exercise, the value assigned to a  was of string type (i.e., text) 
# while the value of b  was an integer (i.e., whole number), and you cannot add strings with integers. Therefore we needed to convert the string to an integer using the int()  addition operation's built-in function to be possible.