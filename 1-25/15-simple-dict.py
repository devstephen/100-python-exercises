# Explanation 1: 

# Using curly brackets is one way to create a dictionary.
new_dict = {
    "a": 1,
    'b': 2
}

print(new_dict)


# Explanation 2:

# A dict  function is another way to create a dictionary. dict  is also used to convert other objects to a dictionary.
another_dict = dict( a = 1, b = 2 )
print(another_dict)

